const _byCode = {};
const _nameIndex = [];
const _byFamily = {};  // NEW: group by color family

// Color family detection from hex
function _detectFamily(hex) {
  if (!hex) return 'neutral';
  const h = hex.replace('#', '');
  const r = parseInt(h.substring(0, 2), 16);
  const g = parseInt(h.substring(2, 4), 16);
  const b = parseInt(h.substring(4, 6), 16);

  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  const diff = max - min;
  const sum = r + g + b;

  // Achromatic check (gray, white, black)
  if (diff < 20) {
    if (sum > 650) return 'white';
    if (sum < 120) return 'black';
    return 'gray';
  }

  // Chromatic — determine hue
  let hue;
  if (max === r) hue = ((g - b) / diff) % 6;
  else if (max === g) hue = (b - r) / diff + 2;
  else hue = (r - g) / diff + 4;
  hue = Math.round(hue * 60);
  if (hue < 0) hue += 360;

  if (hue < 15 || hue >= 345) return 'red';
  if (hue < 45) return 'orange';
  if (hue < 70) return 'yellow';
  if (hue < 165) return 'green';
  if (hue < 195) return 'teal';
  if (hue < 260) return 'blue';
  if (hue < 290) return 'purple';
  if (hue < 345) return 'pink';
  return 'red';
}

for (let i = 0; i < TCX_COLORS.length; i++) {
  const entry = TCX_COLORS[i];
  const normCode = entry.c.replace(/\s+/g, '').toUpperCase();

  if (!_byCode[normCode]) {
    _byCode[normCode] = entry;

    // Color family indexing
    const family = _detectFamily(entry.h);
    if (!_byFamily[family]) _byFamily[family] = [];
    _byFamily[family].push(entry);
  }

  _nameIndex.push({
    idx: i,
    lower: entry.n.toLowerCase(),
    code: normCode,
  });
}

